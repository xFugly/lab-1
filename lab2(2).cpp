#include <iostream>
using namespace std;

int main() {
    // 1. Попросите пользователя ввести целое положительное число
    int number;
    while (true) {
        cout << "Введите целое положительное число: ";
        cin >> number;
        
        // Проверяем, является ли число положительным
        if (number > 0) {
            break;  // Выходим из цикла, если число положительное
        } else {
            cout << "Ошибка! Число должно быть положительным. Попробуйте снова." << endl;
        }
    }
    
    // 2. Выведите сумму всех чисел от 1 до введённого числа
    int sum = 0;
    for (int i = 1; i <= number; i++) {
        sum += i;
    }
    cout << "Сумма чисел от 1 до " << number << " = " << sum << endl;
    
    // 3. Создайте массив из 10 чисел на Ваше усмотрение
    int numbers[10] = {5, 12, 8, 3, 15, 7, 9, 11, 6, 4};
    
    // 4. Выведите все 10 чисел из массива в консоль
    cout << "Массив чисел: ";
    for (int i = 0; i < 10; i++) {
        cout << numbers[i];
        if (i < 9) {
            cout << " ";  // Добавляем пробел между числами, кроме последнего
        }
    }
    cout << endl;
    
    // 5. Выведите числа, стоящие на чётных позициях массива (0, 2, 4, 6, 8)
    cout << "Числа на четных позициях: ";
    for (int i = 0; i < 10; i++) {
        if (i % 2 == 0) {  // Проверяем четность позиции с помощью операции %
            cout << numbers[i] << " ";
        }
    }
    cout << endl;
    
    // 6. Выведите сумму элементов массива, стоящих на нечётных позициях (1, 3, 5, 7, 9)
    int oddPositionsSum = 0;
    for (int i = 0; i < 10; i++) {
        if (i % 2 != 0) {  // Проверяем нечетность позиции с помощью операции %
            oddPositionsSum += numbers[i];
        }
    }
    cout << "Сумма элементов на нечетных позициях: " << oddPositionsSum << endl;
    
    return 0;
}