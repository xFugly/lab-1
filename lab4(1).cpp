#include <iostream>
#include <string>
using namespace std;

// 1. Создайте класс оружия со следующими полями: название оружия, наносимый урон и его вес
class Weapon {
private:
    string name;    // Название оружия
    int damage;     // Наносимый урон
    float weight;   // Вес оружия

public:
    // 2. Создайте конструктор, принимающий 3 параметра и использующий их для инициализации переменных класса
    Weapon(string weaponName, int weaponDamage, float weaponWeight) {
        name = weaponName;
        damage = weaponDamage;
        weight = weaponWeight;
        cout << "Создан объект с параметрами: " << name << endl;
    }

    // 3. Создайте конструктор без параметров, который будет вызывать конструктор из предыдущего пункта,
    // передавая туда какие-то значения по умолчанию
    Weapon() : Weapon("Стандартный меч", 10, 2.5f) {
        cout << "Создан объект через конструктор без параметров" << endl;
    }

    // Метод для вывода информации об оружии
    void displayInfo() {
        cout << "Оружие: " << name << endl;
        cout << "Урон: " << damage << endl;
        cout << "Вес: " << weight << " кг" << endl;
        cout << "-------------------" << endl;
    }
};

int main() {
    cout << "=== Создание объектов класса Weapon ===" << endl;
    
    // 4. Создайте 2 объекта, используя конструктор с параметрами и без
    
    // Создание объекта через конструктор с параметрами
    Weapon sword("Огненный меч", 25, 3.2f);
    
    // Создание объекта через конструктор без параметров
    Weapon defaultWeapon;
    
    cout << "\n=== Вывод информации об объектах ===" << endl;
    
    // 5. Выведите значения всех переменных обоих экземпляров в консоль
    cout << "Первый объект (создан с параметрами):" << endl;
    sword.displayInfo();
    
    cout << "Второй объект (создан без параметров):" << endl;
    defaultWeapon.displayInfo();
    
    return 0;
}